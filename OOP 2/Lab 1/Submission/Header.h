#pragma once
#include <iostream>
using namespace std;

int division(int count1, int count2) {
	return count1 / count2;
}
int multiplication(int count1, int count2) {
	return count1 * count2;
}
int subtraction(int count1, int count2) {
	return count1 - count2;
}
int addition(int count1, int count2) {
	return count1 + count2;
}